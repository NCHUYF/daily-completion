LPCTSTR LocaleData::getColumnText(Locale* locale, ColumnType column_type, int column_index)
{
	if (TASK_COLUMNS_ARRAY[TaskField::TF_WORK].english == NULL)
	{
		init_array();
	}

	// 取得列
	LocaleData::COLUMN_TEXT* textArray = NULL;

	if (TASK_COLUMNS == column_type)
		textArray = TASK_COLUMNS_ARRAY;
	else if (RESOURCE_COLUMNS == column_type)
		textArray = RESOURCE_COLUMNS_ARRAY;
	else if (ASSIGNMENT_COLUMNS == column_type)
		textArray = ASSIGNMENT_COLUMNS_ARRAY;
	else if (CONSTRAINT_COLUMNS == column_type)
		textArray = CONSTRAINT_COLUMNS_ARRAY;
	else
		throw /*BwProj::*/toolkit::Exception(CStringToolkit::IntToStr(column_type) + L"类型无效。");

	// 返回列的值
	LPCTSTR retstr = textArray[column_index].english;
	if (!_tcsncmp(locale->getLanguageCode(), L"Chinese", 7)) {
		if (textArray[column_index].chinese[0] != NULL)
			retstr = textArray[column_index].chinese;
	}

	return retstr;
}

