/**
* Retrieve an instance of the TaskField class based on the data read from an
* MS Project file.
*
* @param value value from an MS Project file
* @return TaskField instance
*/
TaskField MPPTaskField14::getInstance(int value)
{
	init();

	TaskField result = (TaskField )- 1;

    if (value >= 0 && value < MAX_VALUE)
    {
        result = FIELD_ARRAY[value];
    }
    else
    {
        if ((value & 0x8000) != 0)
        {
        int baseValue = TaskField::TF_ENTERPRISE_CUSTOM_FIELD1;
        int id = baseValue + (value & 0xFFF);
        result = (TaskField)id;
        }
    }

    return (result);
}

